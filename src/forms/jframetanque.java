    private void jbtexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtexcluirActionPerformed
        int cont = 0;
        DefaultTableModel modelo = (DefaultTableModel) jtabelatanque.getModel();
        List<tanque> list = new ArrayList<tanque>();
        ArrayList<tanque> nova_list = new ArrayList<tanque>();
        linha = jtabelatanque.getSelectedRow();
        Object nome = modelo.getValueAt(linha, 0);
        list = arq.getTodosTanque();

        for (int i = 0; i < list.size(); i++) {
            if (!(list.get(i).getNumero_identificacao().equals(txtidentificador.getText()))) {
                nova_list.add(list.get(i));
            }
            cont++;
        }
        txtnumero_identtanque.setText(String.valueOf(cont));
        try {
            arqout.recadastrar_todos(nova_list);
        } catch (IOException ex) {
            Logger.getLogger(jfmbomba.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtcap.setText(null);
        txtcomb.setText(null);
        txtidentificador.setText(null);
        modelo.removeRow(linha);
        JOptionPane.showMessageDialog(null, "Tanque apagada com sucesso");

    }//GEN-LAST:event_jbtexcluirActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        List<tanque> lista = new ArrayList<tanque>();
        int cont = 1;

        lista = arq.getTodosTanque();
        DefaultTableModel modelo = (DefaultTableModel) jtabelatanque.getModel();

        for (int i = jtabelatanque.getRowCount() - 1; i >= 0; --i) {
            modelo.removeRow(i);
        }

        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(lista.get(i).gettanque());
            cont++;
        }

        txtnumero_identtanque.setText(String.valueOf(cont));
    }//GEN-LAST:event_formWindowOpened

