    private void jbotaoaddtanqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbotaoaddtanqueActionPerformed

        int cont = 1;
        tq = new tanque(txtnumero_identtanque.getText(),
                Integer.parseInt(txtcapacidade.getText()),
                jcbcombustivel.getSelectedItem().toString());
        try {
            arqout.Adicionar(tq);
        } catch (IOException ex) {
            Logger.getLogger(jframetanque.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(null, "Tanque Inserido com sucesso!");

        txtidentificador.setText(null);
        txtcap.setText(null);
        txtcomb.setText(null);

        DefaultTableModel modelo = (DefaultTableModel) jtabelatanque.getModel();

        if (jtabelatanque.getRowCount() > 0) {
            for (int i = jtabelatanque.getRowCount() - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
        }

        List<tanque> listat2 = new ArrayList<tanque>();
        listat2 = arq.getTodosTanque();

        for (int i = 0; i < listat2.size(); i++) {
            modelo.addRow(listat2.get(i).gettanque());
            cont++;
        }

        txtnumero_identtanque.setText(String.valueOf(cont));
        txtcapacidade.setText(null);
        jcbcombustivel.setSelectedIndex(-1);


    }//GEN-LAST:event_jbotaoaddtanqueActionPerformed

